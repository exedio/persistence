package org.junit;

import org.junit.jupiter.api.Assumptions;

/**
 * This is a replacement of the respective class in JUnit 4.
 * Allows switching to JUnit 5 without extensive changes in the project.
 */
public final class Assume
{
	public static void assumeTrue(final boolean assumption)
	{
		Assumptions.assumeTrue(assumption);
	}


	private Assume()
	{
		// prevent instantiation
	}
}
