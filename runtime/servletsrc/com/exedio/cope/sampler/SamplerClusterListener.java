package com.exedio.cope.sampler;

import com.exedio.cope.ClusterListenerInfo;
import com.exedio.cope.LongField;
import com.exedio.cope.SetValue;
import com.exedio.cope.pattern.Composite;

final class SamplerClusterListener extends Composite
{
	private static final long serialVersionUID = 1l;

	private static final LongField exception    = new LongField().min(0);
	private static final LongField missingMagic = new LongField().min(0);
	private static final LongField wrongSecret  = new LongField().min(0);
	private static final LongField fromMyself   = new LongField().min(0);

	public SamplerClusterListener(final ClusterListenerInfo info)
	{
		this(
			exception   .map(info.getException()),
			missingMagic.map(info.getMissingMagic()),
			wrongSecret .map(info.getWrongSecret()),
			fromMyself  .map(info.getFromMyself()));
	}

	// TODO to be generated by the instrumentor
	private SamplerClusterListener(final SetValue... setValues)
	{
		super(setValues);
	}
}
